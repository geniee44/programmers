SELECT AO.ANIMAL_ID, AO.NAME
FROM ANIMAL_OUTS AO
    JOIN ANIMAL_INS AI ON AI.ANIMAL_ID = AO.ANIMAL_ID
WHERE AI.DATETIME > AO.DATETIME
ORDER BY AI.DATETIME;

