SELECT YEAR(O.SALES_DATE) AS YEAR, MONTH(O.SALES_DATE) AS MONTH,
    COUNT(DISTINCT U.USER_ID) AS PURCHASED_USERS,
    ROUND(COUNT(DISTINCT U.USER_ID) / (SELECT COUNT(DISTINCT UI.USER_ID) FROM USER_INFO UI WHERE YEAR(UI.JOINED)=2021), 1) AS PURCHASED_RATIO
FROM ONLINE_SALE O
    JOIN USER_INFO U ON U.USER_ID = O.USER_ID AND YEAR(U.JOINED) = 2021
GROUP BY YEAR, MONTH
ORDER BY YEAR, MONTH
